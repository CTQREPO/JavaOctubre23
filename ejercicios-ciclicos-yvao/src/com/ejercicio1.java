package com;


public class ejercicio1 {
	public static void main(String[] args) {
		
		
		for(int i = 1; i<=10; i++) { // la i es de donde inica el ciclo, 
									//i<=10 es el numero de veces que se van a hacer los ciclo
			                        // i++ es que ira avanzando de uno en uno
				System.out.println(12+ "*" + i + "=" + (12*i));  //12 es un numero fijo que multiplica al 1 incrementando
																// despues del igual sacara el resultado de 12* el incremento
			}
		}
		
	}

